let timerInterval;
let timerSeconds = 0;
let activeOcrUrl = null; // Variable to store working URL

// Function to convert MM:SS format to seconds with validation (Prevents NaN)
function timeToSeconds(timeStr) {
    if (!timeStr || typeof timeStr !== 'string' || !timeStr.includes(':')) {
        return 0; 
    }
    const [minutes, seconds] = timeStr.split(':').map(Number);
    if (isNaN(minutes) || isNaN(seconds)) {
        return 0; 
    }
    return minutes * 60 + seconds;
}

// Function to convert seconds to MM:SS format with validation
function secondsToTime(seconds) {
    if (isNaN(seconds) || seconds <= 0) return "00:00"; 
    const minutes = Math.floor(seconds / 60);
    const secs = seconds % 60;
    return `${minutes.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}`;
}

// Function to start timer from initial time + 2 seconds
function startTimer(initialTime) {
    if (timerInterval) {
        clearInterval(timerInterval); 
    }
    timerSeconds = timeToSeconds(initialTime) + 2; 
    document.getElementById('timer').textContent = secondsToTime(timerSeconds);
    
    timerInterval = setInterval(() => {
        timerSeconds++;
        document.getElementById('timer').textContent = secondsToTime(timerSeconds);
    }, 1000);
}

// Function to find which URL responds (Localhost or IP)
async function determineOcrUrl() {
    // Option 1: Try localhost connection
    try {
        const resLocal = await fetch('http://localhost:14337/MLBB.json');
        if (resLocal.ok) return 'http://localhost:14337/MLBB.json';
    } catch (e) {
        console.log("Localhost not responding, trying IP Host...");
    }

    // Option 2: Read serverip.txt generated by server.js
    try {
        // Add cache buster when reading IP to avoid getting stuck on old IP
        const resIp = await fetch(`/serverip.txt?t=${new Date().getTime()}`);
        if (resIp.ok) {
            const hostIp = await resIp.text();
            const lanUrl = `http://${hostIp.trim()}:14337/MLBB.json`;
            
            const resLan = await fetch(lanUrl);
            if (resLan.ok) return lanUrl;
        }
    } catch (e) {
        console.log("Failed to read IP from serverip.txt or LAN connection denied.");
    }

    return null; 
}

// Function to fetch JSON data and update display
async function fetchGameData() {
    try {
        if (!activeOcrUrl) {
            activeOcrUrl = await determineOcrUrl();
            
            if (!activeOcrUrl) {
                console.error('OCR data not found on Localhost or LAN IP.');
                return; 
            }
            console.log(`OCR connection successful using URL: ${activeOcrUrl}`);
        }

        // --- MAIN FIX: Cache-Busting ---
        // Add unique timestamp so browser treats it as new URL every second
        const cacheBuster = new Date().getTime();
        const fetchUrl = `${activeOcrUrl}?t=${cacheBuster}`;

        const response = await fetch(fetchUrl, {
            method: 'GET',
            headers: {
                'Cache-Control': 'no-cache, no-store, must-revalidate',
                'Pragma': 'no-cache',
                'Expires': '0'
            },
            cache: 'no-store' // Force browser to bypass cache
        });

        if (!response.ok) {
            throw new Error(`Failed to fetch data, HTTP status: ${response.status}`);
        }

        const data = await response.json();

        const elementsToUpdate = {
            'homescore': data.killscore1,
            'awayscore': data.killscore2,
            'bluegold': data.gold1,
            'redgold': data.gold2,
            'bluetower': data.turret1,
            'redtower': data.turret2,
            'bluelord': data.lord1,
            'redlord': data.lord2
        };

        // Update values for HTML elements
        for (const [id, value] of Object.entries(elementsToUpdate)) {
            const el = document.getElementById(id);
            if (el && value !== undefined) {
                el.textContent = value;
            }
        }

        // --- NEW TIMER LOGIC ---
        const currentTimerSeconds = timeToSeconds(data.timer);

        if (currentTimerSeconds === 0) {
            // If timer in game is still 00:00 or OCR fails to read (NaN)
            document.getElementById('timer').textContent = "00:00";
            
            // Stop local interval so fetchGameData keeps checking/refreshing JSON data every second
            if (timerInterval) {
                clearInterval(timerInterval);
                timerInterval = null; 
            }
        } else {
            // If timer is already read and running (> 0)
            if (!timerInterval) {
                // Start local timer interval so it runs smoothly on overlay
                startTimer(data.timer);
            }
        }

    } catch (error) {
        console.error('Error fetching data:', error);
        // Jika koneksi putus, reset activeOcrUrl agar bisa mencari IP lagi di siklus berikutnya
        activeOcrUrl = null; 
    }
}

// Panggil fetchGameData pertama kali saat halaman dimuat
fetchGameData();

// Perbarui data setiap 1 detik
setInterval(fetchGameData, 1000);